a = 4
b = 2

# 1. Adição
print(a + b)  # Retorna: 6


# 2. Subtração
print(a - b)  # 2


# 3. Multiplicação
print(a * b)  # 8


# 4. Divisão (retorna valor decimal real)
print(a / b)  # 2.0


# 5. Divisão (retorna valor inteiro)
print(a // b)  # 2


# 6. Módulo (retorna o resto da divisão)
print(a % b)  # 0


# 7. Exponencial
print(a ** b) # 16